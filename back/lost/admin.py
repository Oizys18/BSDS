from django.contrib import admin
from .models import LostPosting, LostImage


class LostPostingModelAdmin(admin.ModelAdmin):
    list_display = 'id', 'category'


class LostImageModelAdmin(admin.ModelAdmin):
    list_display = 'id', 'image'


admin.site.register(LostPosting, LostPostingModelAdmin)
admin.site.register(LostImage, LostImageModelAdmin)
